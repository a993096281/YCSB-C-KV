#include "db/db_factory.h"

#include <string>
#include "db/basic_db.h"
#include "db/scaledkv_db.h"

using namespace std;
using ycsbc::DB;
using ycsbc::DBFactory;

DB* DBFactory::CreateDB(utils::Properties &props) {
  if (props["dbname"] == "basic") {
    return new BasicDB;
  } else if (props["dbname"] == "scaledkv") {
    std::string dbpath = props.GetProperty("dbpath","/tmp/test-scaledkv");
    return new ScaledKV(dbpath.c_str(), props);
  } else return NULL;
}

